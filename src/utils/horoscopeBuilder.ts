export function buildHoroscopeThemeScript(config: HoroscopeThemeConfig): string {
  const { themeName, nameEn, cardDir, accent, bodyBg } = config;
  
  return `#!/bin/bash
# Horoscope Create Custom Theme Script
# Automatically generated by BulkVideoCreatorApp

BASE_DIR="${HOROSCOPE_DIR}"
cd "$BASE_DIR" || { echo "❌ ไม่พบโฟลเดอร์ $BASE_DIR"; exit 1; }

echo "🎭 เริ่มสร้างธีมใหม่: ${themeName}"
echo "----------------------------------------"

cat << 'EOF' > _temp_run_theme.py
import sys, os
from theme_config import get_all_themes, save_custom_theme, _next_custom_key
from pathlib import Path

script_dir = Path(__file__).parent.resolve()

theme_data = {
    "name": "${themeName}",
    "name_en": "${nameEn}",
    "card_dir": "${cardDir}",
    "desc": "${themeName}",
    "vars": {
        "--accent": "${accent}",
        "--body-bg": "${bodyBg}"
    }
}

key = _next_custom_key(script_dir)
save_custom_theme(script_dir, key, theme_data)

print(f"✅ บันทึกธีม {key} ('{theme_data['name']}') ลงใน custom_themes.json แล้ว!")
print(f"คุณสามารถเรียกใช้มันได้โดยระบุพิมพ์ --theme {key} ในสคริปต์หลัก")
EOF

python3 _temp_run_theme.py
rm _temp_run_theme.py

echo "----------------------------------------"
echo "✅ เสร็จสิ้น!"
`;
}
